export function t(lang: AppLang, key: string, params?: Record<string, string | number>): string {
  const dict = translations[lang] ?? translations.en;
  let str = dict[key] ?? translations.en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
  }
  return str;
}

export function translateLanguageNameToCode(name: string): string {
  return Object.keys(translations.en).find(key => translations.en[key as keyof typeof translations.en] === name) ?? name;
}

export function translateLanguageCodeToName(code: string): string {
  return translations.en[code as keyof typeof translations.en] ?? code;
}

